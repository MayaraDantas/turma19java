package Lista7;

public class Aviao {

	private int numeroDePassagem;
	private double pesoBagagem;
	private int numeroPoltrona;

	public int getNumeroDePassagem() {
		return numeroDePassagem;
	}

	public void setNumeroDePassagem(int numeroDePassagem) {
		this.numeroDePassagem = numeroDePassagem;
	}

	public double getPesoBagagem() {
		return pesoBagagem;
	}

	public void setPesoBagagem(double pesoBagagem) {
		this.pesoBagagem = pesoBagagem;
	}

	public int getNumeroPoltrona() {
		return numeroPoltrona;
	}

	public void setNumeroPoltrona(int numeroPoltrona) {
		this.numeroPoltrona = numeroPoltrona;
	}

}
